export function cleanSentenceForN5(text: string): string {
  if (!text) return text;
  let result = text;

  // 1. Strip parenthesized kana for N4+ kanji words
  // e.g. "英語(えいご)" -> "えいご" or "英語（えいご）" -> "えいご"
  const parenthesizedRegex = /([\u4e00-\u9faf]+)(?:\(([^)]+)\)|（([^）]+)）)/g;
  result = result.replace(parenthesizedRegex, (match, kanji, kana1, kana2) => {
    const kana = kana1 || kana2;
    if (getWordKanjiLevel(kanji) === 'N4+') {
      return kana;
    }
    return match;
  });

  // 2. Perform replacements
  N4_REPLACEMENTS.forEach(([word, reading]) => {
    result = result.split(word).join(reading);
  });

  return result;
}

